// while loop in number 
let index = 1;
while (index <= 10) {
    // console.log(`The value of index is: ${index}`);
    index++
}


// while loop in Array
let myArr = ['Rishi', 'Gagan', 'Deepak'];
let myIndex = 0;
while (myIndex < myArr.length) {
    // console.log(`The value of arr is: ${myArr[myIndex]}`);
    myIndex++
}


// do while loop

let score = 11;

do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10)